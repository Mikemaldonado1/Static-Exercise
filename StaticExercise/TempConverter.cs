using System;
namespace StaticExercise;
using System.Collections.Generic;

	internal class TempConverter
	{
		

		public static double FahrenheitToCelsius (double num1)
		{
			return ((num1-32)*5/9);

		}





		public static double CelsiusToFahrenheit (double Num1)
		{

		 return ((Num1* 1.8) + 32);

		}

	




	}

